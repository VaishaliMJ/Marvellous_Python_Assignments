#--------------------------------------------------------------
# Outputs given string on console
#--------------------------------------------------------------
def PrintString(no):
    print("-------------------------------------------")
    for i in range(no):
        print("(",i+1,").","Marvellous")
    print("-------------------------------------------")
#-------------------------------------------------------------------
# This function accepts a number and 
# calls PrintString(no) function    
#------------------------------------------------------------------
def main():
    no = 5
    PrintString(no)
#------------------------------------------------------------------

#------------------------------------------------------------------  
# Entry point of program
if __name__=="__main__":
    main()
