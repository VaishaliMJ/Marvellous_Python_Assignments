#--------------------------------------------------------------
# Outputs given string on console
#--------------------------------------------------------------
def PrintString(no):
    print("-------------------------------------------")
    for i in range(no):
        print("(",i+1,").","Marvellous")
    print("-------------------------------------------")
#-------------------------------------------------------------------
# This function calls PrintString(no) with one parameter    
#------------------------------------------------------------------
def main():
    no = 5
    PrintString(no)
#------------------------------------------------------------------
#------------------------------------------------------------------  
# Entry point of program
#------------------------------------------------------------------s
if __name__=="__main__":
    main()
