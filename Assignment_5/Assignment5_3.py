#-----------------------------------------------------------------------------------
# Checks if person is eligible to vote
#-----------------------------------------------------------------------------------
def eligibleToVote(age):
    print("------------------------------------------------------")
    if age >= 18:
        print("Eligible to vote")
    else:    
        print("Not eligible to vote")
  
    print("------------------------------------------------------")
#-----------------------------------------------------------------------------------
# This function accepts a age from user and calls function eligibleToVote(age)
#-----------------------------------------------------------------------------------
def main():
    age=int(input("Enter person's age:"))
    eligibleToVote(age)
#-----------------------------------------------------------------------------------
# This is main entry point of the program
#-----------------------------------------------------------------------------------
if __name__=="__main__":
    main()
