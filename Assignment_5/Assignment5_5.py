#-----------------------------------------------------------------------------------
#This function checks if given number is even or odd
#-----------------------------------------------------------------------------------
def findEvenOrOdd(num):    
    print("------------------------------------------------------")
    if (num%2==0):
        print(f"{num} is an Even number")
    else:
        print(f"{num} is an Odd number")
    print("------------------------------------------------------")
#-----------------------------------------------------------------------------------
# This function accepts a number from user and calls function findEvenOrOdd(number) 
#-----------------------------------------------------------------------------------
def main():
    number=int(input("Enter a number:")) 
    findEvenOrOdd(number)  
#-----------------------------------------------------------------------------------
# This is main entry point of the program
#-----------------------------------------------------------------------------------
if __name__=="__main__":
    main()
