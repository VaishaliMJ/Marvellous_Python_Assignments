#-------------------------------------------------------------------------------
# Print numbers upto 100 using while loop
#-------------------------------------------------------------------------------
def printNumbers():
    print("\n\nPrinting numbers upto 50 ...\n\n")
    print("------------------------------------------------------")
    count = 1
    while count <= 50:
        if count%10==0:
            print(count,"\n\n")
        else:    
            print(count,end="  ")
        count+=1
    print()   
    print("------------------------------------------------------")
 
#-------------------------------------------------------------------------------
# Calls printNumbers()
#-------------------------------------------------------------------------------
def main():
    printNumbers()
#-------------------------------------------------------------------------------
# Entry point of program
#-------------------------------------------------------------------------------
if __name__=="__main__":   
    main()
