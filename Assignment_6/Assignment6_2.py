#-------------------------------------------------------------------------------
# This function finds and Add even numbers upto 100
#-------------------------------------------------------------------------------
def findAndAddEvenNumbers():
    sumEvenNums=0
    for num in range(2,101,2):
        sumEvenNums+=num
    return sumEvenNums 
#-------------------------------------------------------------------------------
# Calls findAndAddEvenNumbers()
#-------------------------------------------------------------------------------
def main():
    sumEvenNums=findAndAddEvenNumbers()
    print("------------------------------------------------------")
    print("\nSum of even numbers between 1 to 100 is :",sumEvenNums)
    print("------------------------------------------------------")
#-------------------------------------------------------------------------------
# Entry point of program
#-------------------------------------------------------------------------------
if __name__=="__main__":   
    main()
