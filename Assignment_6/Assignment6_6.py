STAR="*"
#-------------------------------------------------------------------------------
# This function prints * in triangle pattern
#-------------------------------------------------------------------------------
def printStars(num):
    for row in range(num):
        for column in range(num):
            if (row>=column):
                print(STAR,end=" ")
        print()       
            
#-------------------------------------------------------------------------------
# Calls printStars(number)
#-------------------------------------------------------------------------------
def main():
    number=int(input("Enter the number to print stars :"))
    print("------------------------------------------------------")
    printStars(number)
    print("------------------------------------------------------")
    
#-------------------------------------------------------------------------------
# Entry point of program
#-------------------------------------------------------------------------------
if __name__=="__main__":   
    main()
