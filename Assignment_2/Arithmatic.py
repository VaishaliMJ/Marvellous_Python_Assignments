#Addition function with two parameters
def Add(num1,num2):
    return num1 + num2


#Subtraction function with two parameters
def Sub(num1,num2):
    return num1 - num2

#Multiplication function with two parameters
def Mult(num1,num2):
    return num1 * num2

#Division function with two parameters
def Div(num1,num2):
    return num1 / num2




