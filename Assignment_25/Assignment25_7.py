import pandas as pd
BORDER="-"*50
#---------------------------------------------------------------------------------------------------------
#  This function Normalise the 'Age' using Min-Max Scaling
#---------------------------------------------------------------------------------------------------------
def normaliseAgeColumn():
    data={'Age':[18,22,25,30,35]}
    ageDF=pd.DataFrame(data)
    ageDF['Age']=(ageDF['Age']-ageDF['Age'].min())/(ageDF['Age'].max()-ageDF['Age'].min())

    print(BORDER)
    print("Normalised Data Frame")
    print(ageDF)
    print(BORDER)
#---------------------------------------------------------------------------------------------------------
#  Main function calls normaliseColumn() 
#---------------------------------------------------------------------------------------------------------
def main():
    
    #Normalise Age column
    normaliseAgeColumn()
    
#---------------------------------------------------------------------------------------------------------
#Main entry point of the program
#---------------------------------------------------------------------------------------------------------

if __name__=="__main__":
    main()